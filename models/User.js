import mongoose from 'mongoose';

const userSchema = new mongoose.Schema({
    email: {
        type: String,
        required: true,
        unique: true,
        trim: true,
        lowercase: true
    },
    password: {
        type: String,
        required: true
    },
    createdAt: {
        type: Date,
        default: Date.now
    }
});

// Create the model collection instance for the cloud cluster
const User = mongoose.model('User', userSchema);

// 🚨 THIS IS THE LINE NODE IS LOOKING FOR:
export default User;